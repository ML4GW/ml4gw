def compute_match(h1, h2):
    inner_prod = torch.sum(torch.conj(h1) * h2)
    norm1 = torch.sqrt(torch.sum(torch.conj(h1) * h1))
    norm2 = torch.sqrt(torch.sum(torch.conj(h2) * h2))
    return (inner_prod.abs() / (norm1 * norm2)).real


@pytest.mark.parametrize(
    "name, chirp_mass, mass_ratio, chi1, chi2, distance, phase, theta_jn",
    ALIGNED_TEST_CASES,
)
def test_taylor_f2(
    name,
    chirp_mass,
    mass_ratio,
    chi1,
    chi2,
    distance,
    phase,
    theta_jn,
    f_ref,
    sample_rate,
):
    chirp_mass = torch.tensor([chirp_mass], dtype=torch.float64)
    mass_ratio = torch.tensor([mass_ratio], dtype=torch.float64)
    chi1 = torch.tensor([chi1], dtype=torch.float64)
    chi2 = torch.tensor([chi2], dtype=torch.float64)
    distance = torch.tensor([distance], dtype=torch.float64)
    phase = torch.tensor([phase], dtype=torch.float64)
    theta_jn = torch.tensor([theta_jn], dtype=torch.float64)

    # Test that a difference in the length of a
    # parameter tensor raises an error
    with pytest.raises(ValueError, match="Tensors must have same batch size"):
        torch_freqs = torch.arange(100)
        waveforms.TaylorF2()(
            torch_freqs,
            chirp_mass[1:],
            mass_ratio,
            chi1,
            chi2,
            distance,
            phase,
            theta_jn,
            f_ref,
        )

    mass_1, mass_2 = chirp_mass_and_mass_ratio_to_components(
        chirp_mass, mass_ratio
    )

    # construct lalinference params
    params = {
        "m1": mass_1.item() * lal.MSUN_SI,
        "m2": mass_2.item() * lal.MSUN_SI,
        "S1x": 0,
        "S1y": 0,
        "S1z": chi1.item(),
        "S2x": 0,
        "S2y": 0,
        "S2z": chi2.item(),
        "distance": (distance.item() * u.Mpc).to("m").value,
        "inclination": theta_jn.item(),
        "phiRef": phase.item(),
        "longAscNodes": 0.0,
        "eccentricity": 0.0,
        "meanPerAno": 0.0,
        "deltaF": 1.0 / sample_rate,
        "f_min": 20,
        "f_ref": f_ref,
        "f_max": 300,
        "approximant": lalsimulation.TaylorF2,
        "LALpars": lal.CreateDict(),
    }
    hp_lal, hc_lal = lalsimulation.SimInspiralChooseFDWaveform(**params)

    # reconstruct frequencies generated by
    # lal and filter based on fmin and fmax
    lal_freqs = np.array(
        [hp_lal.f0 + ii * hp_lal.deltaF for ii in range(len(hp_lal.data.data))]
    )

    lal_mask = (lal_freqs > params["f_min"]) & (lal_freqs < params["f_max"])

    lal_freqs = lal_freqs[lal_mask]
    torch_freqs = torch.tensor(lal_freqs, dtype=torch.float64)

    # generate waveforms using ml4gw
    hc_ml4gw, hp_ml4gw = waveforms.TaylorF2()(
        torch_freqs,
        chirp_mass,
        mass_ratio,
        chi1,
        chi2,
        distance,
        phase,
        theta_jn,
        f_ref,
    )

    hc_ml4gw = hc_ml4gw[0]
    hp_ml4gw = hp_ml4gw[0]

    hp_lal_data = hp_lal.data.data[lal_mask]
    hc_lal_data = hc_lal.data.data[lal_mask]

    # ensure no nans
    assert not torch.any(torch.isnan(hc_ml4gw))
    assert not torch.any(torch.isnan(hp_ml4gw))

    assert compute_match(hp_ml4gw, torch.tensor(hp_lal_data)) > TARGET_OVERLAP
    assert compute_match(hc_ml4gw, torch.tensor(hc_lal_data)) > TARGET_OVERLAP

    # taylor f2 is symmetric w.r.t m1 --> m2 flip.
    # so test that the waveforms are the same when m1 and m2
    # (and corresponding chi1, chi2 are flipped)
    # are flipped this can be done by flipping mass ratio
    hc_ml4gw, hp_ml4gw = waveforms.TaylorF2()(
        torch_freqs,
        chirp_mass,
        1 / mass_ratio,
        chi2,
        chi1,
        distance,
        phase,
        theta_jn,
        f_ref,
    )

    hc_ml4gw = hc_ml4gw[0]
    hp_ml4gw = hp_ml4gw[0]

    assert compute_match(hp_ml4gw, torch.tensor(hp_lal_data)) > TARGET_OVERLAP
    assert compute_match(hc_ml4gw, torch.tensor(hc_lal_data)) > TARGET_OVERLAP


