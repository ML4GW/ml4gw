@pytest.mark.parametrize(
    "name, chirp_mass, mass_ratio, chi1, chi2, distance, phase, theta_jn",
    PRECESSING_TEST_CASES,
)
def test_phenom_p(
    name,
    chirp_mass,
    mass_ratio,
    chi1,
    chi2,
    distance,
    phase,
    theta_jn,
    sample_rate,
    f_ref,
):
    chirp_mass = torch.tensor([chirp_mass], dtype=torch.float64)
    mass_ratio = torch.tensor([mass_ratio], dtype=torch.float64)
    chi1x, chi1y, chi1z = [torch.tensor(c, dtype=torch.float64) for c in chi1]
    chi2x, chi2y, chi2z = [torch.tensor(c, dtype=torch.float64) for c in chi2]
    distance = torch.tensor([distance], dtype=torch.float64)
    theta_jn = torch.tensor([theta_jn], dtype=torch.float64)
    phase = torch.tensor([phase], dtype=torch.float64)

    mass_1, mass_2 = chirp_mass_and_mass_ratio_to_components(
        chirp_mass, mass_ratio
    )

    # Test `tc = None`
    torch_freqs = torch.arange(100)
    hc_ml4gw, hp_ml4gw = waveforms.IMRPhenomPv2()(
        torch_freqs,
        chirp_mass,
        mass_ratio,
        chi1x,
        chi1y,
        chi1z,
        chi2x,
        chi2y,
        chi2z,
        distance,
        phase,
        theta_jn,
        f_ref,
        tc=None,
    )

    tc = 0.0

    # construct lalinference params
    params = {
        "m1": mass_1.item() * lal.MSUN_SI,
        "m2": mass_2.item() * lal.MSUN_SI,
        "S1x": chi1x.item(),
        "S1y": chi1y.item(),
        "S1z": chi1z.item(),
        "S2x": chi2x.item(),
        "S2y": chi2y.item(),
        "S2z": chi2z.item(),
        "distance": (distance.item() * u.Mpc).to("m").value,
        "inclination": theta_jn.item(),
        "phiRef": phase.item(),
        "longAscNodes": 0.0,
        "eccentricity": 0.0,
        "meanPerAno": 0.0,
        "deltaF": 1.0 / sample_rate,
        "f_min": 20.0,
        "f_ref": f_ref,
        "f_max": 300,
        "approximant": lalsimulation.IMRPhenomPv2,
        "LALpars": lal.CreateDict(),
    }
    hp_lal, hc_lal = lalsimulation.SimInspiralChooseFDWaveform(**params)

    # reconstruct frequencies generated by
    # lal and filter based on fmin and fmax
    lal_freqs = np.array(
        [hp_lal.f0 + ii * hp_lal.deltaF for ii in range(len(hp_lal.data.data))]
    )

    lal_mask = (lal_freqs > params["f_min"]) & (lal_freqs < params["f_max"])

    lal_freqs = lal_freqs[lal_mask]
    torch_freqs = torch.tensor(lal_freqs, dtype=torch.float64)

    hc_ml4gw, hp_ml4gw = waveforms.IMRPhenomPv2()(
        torch_freqs,
        chirp_mass,
        mass_ratio,
        chi1x,
        chi1y,
        chi1z,
        chi2x,
        chi2y,
        chi2z,
        distance,
        phase,
        theta_jn,
        f_ref,
        torch.tensor([tc]),
    )

    hp_ml4gw = hp_ml4gw[0]
    hc_ml4gw = hc_ml4gw[0]

    hp_lal_data = hp_lal.data.data[lal_mask]
    hc_lal_data = hc_lal.data.data[lal_mask]

    assert compute_match(hp_ml4gw, torch.tensor(hp_lal_data)) > TARGET_OVERLAP
    assert compute_match(hc_ml4gw, torch.tensor(hc_lal_data)) > TARGET_OVERLAP

    # test batched outputs works as expected
    hc_ml4gw, hp_ml4gw = waveforms.IMRPhenomPv2()(
        torch_freqs,
        chirp_mass.repeat(5),
        mass_ratio.repeat(5),
        chi1x.repeat(5),
        chi1y.repeat(5),
        chi1z.repeat(5),
        chi2x.repeat(5),
        chi2y.repeat(5),
        chi2z.repeat(5),
        distance.repeat(5),
        phase.repeat(5),
        theta_jn.repeat(5),
        f_ref,
        torch.tensor([tc]).repeat(5),
    )

    # check batch against lal
    assert (
        compute_match(hp_ml4gw[0], torch.tensor(hp_lal_data)) > TARGET_OVERLAP
    )
    assert (
        compute_match(hc_ml4gw[0], torch.tensor(hc_lal_data)) > TARGET_OVERLAP
    )

    # check batch against each other
    for i in range(4):
        assert compute_match(hp_ml4gw[i], hp_ml4gw[0]) > TARGET_OVERLAP
        assert compute_match(hc_ml4gw[i], hc_ml4gw[0]) > TARGET_OVERLAP

    with pytest.raises(ValueError, match=r"Invalid mode s=-2, l=2, m=3*"):
        waveforms.IMRPhenomPv2().SpinWeightedY(0, 0, -2, 2, 3)
